package ch03_oper;

public class Test06 {
	public static void main(String args[]){
		//관계연산자
		System.out.println("7>5 "+(7>5));//true
		System.out.println(7>=5);//true
		System.out.println(7<5);//false
		System.out.println(7<=5);//false
		System.out.println(3>=3);//true
		System.out.println(3==3);//true
		System.out.println(3!=3);//false
		
		//
		//1~10까지 출력
		for(int i=1; i<10; i++){
			System.out.println("i:"+i);
		}//for end
	}//main end

}//class end
