package ch04_for;

public class Test02 {
	public static void main(String args[]){
		//if,for 
		//1~10까지 짝수만 출력하시오 
		for(int i=1; i<=10; i++){
			if(i%2==0){//짝수
				System.out.print(i+" ");
			}//if
		}//for end
		
		System.out.println();//line skip
		
		//1~100까지 7의 배수를 출력
		for(int i=1;i<=100;i++){
			if(i%7==0){
				System.out.print(i+" ");
			}//if 
		}//for end
		
		System.out.println();//line skip
		
		//1~100까지 합을 구하기 
		
			
		
	}//main end 

}//class end
